package ex_011_While_Loop;

public class Lab_079_basic_while_loop {
    public static void main(String[] args) {
        int a=0; //initialization
        while(a<15) //CONDITION if true then it will go inside the loop
        {
            System.out.println(a);
            a++; //updation
        }
    }
}

