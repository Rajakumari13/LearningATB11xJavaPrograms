package ex_003_Variables_Datatypes;

public class Lab_013_11Mar25Task {
    public static void main(String[] args) {
        final int a=9;

        System.out.printf("9 * 1= %d", a);
        System.out.println();
        System.out.printf("9 * 2= %d", a*2);
        System.out.println();
        System.out.printf("9 * 3= %d", a*3);
        System.out.println();
        System.out.printf("9 * 4= %d", a*4);
        System.out.println();
        System.out.printf("9 * 5= %d", a*5);
        System.out.println();
        System.out.printf("9 * 6= %d", a*6);
        System.out.println();
        System.out.printf("9 * 7= %d\n", a*7);
        //instead of println we can use escape sequence \n)
        System.out.printf("9 * 8= %d \n", a*8);
        System.out.printf("9 * 9= %d\n", a*9);
        System.out.printf("9 * 10= %d\n", a*10);
        /*
        9 table till 9 * 10= 90 using %d
         */
    }
}
