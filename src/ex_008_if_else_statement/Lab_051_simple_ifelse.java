package ex_008_if_else_statement;

public class Lab_051_simple_ifelse {
    public static void main(String[] args) {
        int a=2;
        if (a<=10)
        {
            System.out.println("A is in single digit");
        }
        else if (a<=100)
        {
            System.out.println("A is in two digit");
        }
        else {
            System.out.println("A is in multiple digit");
        }
    }
    }

