package ex_004_Operators;

public class Lab_016_Assign_relationalops {
    public static void main(String[] args) {
        //Used to compare two values. Returns true or false.
       int a=5;
       int b=6;
        System.out.println(a==b);
        System.out.println(a>=b);
        System.out.println(a<=b); //true as less than
        System.out.println(a!=b); //true as not equals
        System.out.println(a>b);
        System.out.println(a<b);
    }
}
